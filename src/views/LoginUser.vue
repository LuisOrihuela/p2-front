<template>
  <div>
    <div class="container col-sm-4" style="@media (max-width: 575px)">
      <form @submit.prevent="login">
        <img src="../assets/icon.png" class="icon" />
        <div class="card-body">
          <h5 class="card-title">Inicia sesión a facturatron</h5>

          <div class="form-group">
            <label for="email">Ingresa tu correo</label>
            <input
              type="email"
              name="email"
              class="form-control"
              id="exampleInputEmail1"
              aria-describedby="emailHelp"
              placeholder="Mail"
              v-model="email"
              value
              required
            />
          </div>

          <div class="form-group">
            <label for="password">Ingresa tu contraseña</label>
            <input
              type="password"
              name="password"
              class="form-control"
              id="exampleInputPassword1"
              placeholder="Password"
              v-model="password"
              value
              required
            />
          </div>
          <div class="d-flex justify-content-center">
            <button class="btn btn-primary disabled" type="submit" name="button">Entrar</button>
            <p>{{ error }}</p>
          </div>
          <br />
          <router-link to="/register">
            <a>No tienes una cuenta? Regístrate.</a>
          </router-link>
        </div>
      </form>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      email: '',
      password: '',
      error: null
    }
  },
  methods: {
    login() {
      this.$store
        .dispatch('login', {
          email: this.email,
          password: this.password
        })
        .then(() => {
          this.$router.push({ name: 'dashboard' })
        })
        .catch(err => {
          console.log(err.response)
          this.error = err.response.data.error
        })
    }
  }
}
</script>

